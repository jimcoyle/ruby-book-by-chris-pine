# how many hours in a year?

puts 'how many hours in a year'
puts (365 * 24)

puts 'how many minutes in a decade'
puts (10 * 365 * 24 * 60)

puts 'my age in seconds'
puts (48 * 365 * 24 * 60 * 60)

# I am 1,111 million seconds old - how old am I?
puts (1111000000 / 60 / 60 / 24 / 365).to_s + ' years old'